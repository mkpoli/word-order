function sortKeys(obj: Strings, reference: string[]): Strings {
	const out: Strings = {};
	for (const k of reference) if (k in obj) out[k] = obj[k];
	for (const k of Object.keys(obj)) if (!(k in out)) out[k] = obj[k];
	return out;
}

const en: Strings = JSON.parse(readFileSync(join(DIR, 'en.json'), 'utf8'));
const enOrder = Object.keys(en);

for (const locale of Object.keys(T)) {
	const file = join(DIR, `${locale}.json`);
	const existing: Strings = JSON.parse(readFileSync(file, 'utf8'));
	let added = 0;
	for (const [k, v] of Object.entries(T[locale])) {
		if (!(k in existing)) {
			existing[k] = v;
			added++;
		}
	}
	const ordered = sortKeys(existing, enOrder);
	writeFileSync(file, JSON.stringify(ordered, null, '\t') + '\n', 'utf8');
	console.log(`${locale}: +${added} keys`);
}
